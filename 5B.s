	AREA PROG,CODE,READONLY
PLACE		RN 1
TEMP		RN 2
COUNT		RN 3
SUM			RN 4
DIVIDEND		RN 5
DIVISOR		RN 6
QUOTIENT	RN 7
ENTRY
	LDR PLACE,=TABLE
	MOV COUNT,#0
	MOV SUM,#0
	
LOOP
	LDRH TEMP,[PLACE],#2
	ADD SUM,SUM,TEMP
	ADD COUNT,COUNT,#1
	CMP COUNT,#10
	BNE LOOP
	
	MOV DIVIDEND,SUM
	MOV QUOTIENT,#0
	MOV DIVISOR,#10
	
BACK
	SUBS DIVIDEND,DIVIDEND,DIVISOR
	ADD QUOTIENT,QUOTIENT,#1
	BPL BACK
	
	
GO B GO


TABLE	DCW 1000,1001,1002,1003,1004,1005,1006,1007,1008,1009
	END