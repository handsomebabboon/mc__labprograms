	AREA PROG,CODE,READONLY
SUM			RN 1
TEMP		RN 2
NUM			RN 3
QUOTIENT	RN 4
DIVISOR		RN 5
DIVIDEND	RN 6
REMAINDER	RN 7
ENTRY
	LDR NUM,=51
	MOV SUM,#0
	MOV DIVISOR,#10
	MOV DIVIDEND,NUM
LOOP BL DIV
	ADD SUM,SUM,REMAINDER
	CMP QUOTIENT,#0
	MOVNE DIVIDEND,QUOTIENT
	BNE LOOP
STOP B STOP

DIV	MOV QUOTIENT,#0
	
LOOP2	
	SUBS DIVIDEND,DIVIDEND,DIVISOR ; HAS TO BE SUBS
	ADDPL QUOTIENT,QUOTIENT,#1
	BPL LOOP2
	ADD REMAINDER,DIVIDEND,DIVISOR
	BX LR
	END