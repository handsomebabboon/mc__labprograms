	AREA PROG,CODE,READONLY
NUM			RN 1
NUMPLUS1	RN 2
PLACE		RN 3
TEMP		RN 4
COUNT		RN 5
ENTRY
	MOV NUM,#0
	MOV NUMPLUS1,#1
	MOV COUNT,#2
	LDR PLACE,=TABLE
	
	STR NUM,[PLACE]
	STR NUMPLUS1,[PLACE]
LOOP
	ADD COUNT,COUNT,#1
	ADD TEMP,NUM,NUMPLUS1
	STR TEMP,[PLACE]
	MOV NUM,NUMPLUS1
	MOV NUMPLUS1,TEMP
	CMP COUNT,#10
	BNE LOOP
GO B GO

	
	AREA T,DATA,READWRITE
TABLE		SPACE 10
	END